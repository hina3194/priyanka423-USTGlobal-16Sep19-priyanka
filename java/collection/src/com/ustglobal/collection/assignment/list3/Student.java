package com.ustglobal.collection.assignment.list3;

public class Student{
	int id;
	String name;
	int rollno;
	String dept;
	String lname;
	public Student(int id, String name, int rollno, String dept, String lname) {
		super();
		this.id = id;
		this.name = name;
		this.rollno = rollno;
		this.dept = dept;
		this.lname = lname;
	}

}
